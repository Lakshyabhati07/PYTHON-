#To compare two numbers
a=int(input("Enter the first number"))
b=int(input("Enter the second number"))
#To compare equality!
print("To compare equality!")
c1=(a==b)
print(c1)
#To compare not equality!
print("To compare not equality!")
c2=(a!=b)
print(c2)
#To compare less than!
print("To compare less than!")
c3=(a<b)
print(c3)
#To compare greater than!
print("To compare greater than!")
c4=(a>b)
print(c4)
#To insert two bits
print("To insert two bits!")
c5=(a<<b)
print(c5)
#To extract two bits!
print("To extract two bits!")
c6=(a>>b)
print(c6)

